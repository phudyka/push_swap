#include "push_swap.h"

static void	swap(t_stack **stack)
{
	t_stack *temp;
	t_stack	*third;

	if (stack == NULL || *stack == NULL
		|| (*stack)->next == NULL)
		return;
	temp = *stack;
	third = (*stack)->next->next;
	*stack = (*stack)->next;
	(*stack)->next = temp;
	(*stack)->next->next = third;
	return;
}

void	sa(t_stack **stack_a)
{
	swap(stack_a);
	ft_putstr_fd("sa\n", 1);
}

void	sb(t_stack **stack_b)
{
	swap(stack_b);
	ft_putstr_fd("sb\n", 1);
}

void	ss(t_stack **stack_a, t_stack **stack_b)
{
	swap(stack_a);
	swap(stack_b);
	ft_putstr_fd("ss\n", 1);
}